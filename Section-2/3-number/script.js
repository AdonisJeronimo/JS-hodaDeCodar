/* NUMBERS

É um tipo de dado númerico que é escrito sem nenhum tipo de aspas. Com a ajuda do typeof podemos verificar o tipo dado
que foi inserido
*/

console.log(typeof 13);
console.log(typeof 1.8);
console.log(typeof -5);

/*
OBS: Sempre que formos representar numeros decimais, utilizaremos PONTO e NÃO VIRGULA. Também é bom entendermos que
não ha necessidade de colocarmos pontos em milhares,centenas e dezenas, sendo apenas necessario informamos quais são
os numeros decimais. Por exemplo:

1.500,50 - Como escrevemos normalmente
1500.50  - Como é escrito no JAVASCRIPT. Utilizando apenas o ponto para informarmos o que são decimais.

Também para compreendermos melhor os numeros e suas operações, irei deixar abaixo os operadores mátematicos utilizados
 que são:

    SOMA            (+)
    SUBTRAÇÃO       (-)
    MULTIPLICAÇÃO   (*)
    DIVISÃO         (/)
    MODULO          (%)
*/

/*
As operações aritmeticas são as mais utilizados e os resultados destas operação produz um novo numero, por exemplo: 
*/

console.log(2 + 2); // resultado: 4
console.log(2 * 2); // resultado: 4
console.log(2 / 2);  // resultado: 1
console.log(2 * 2 - 3);  // resultado: 1

//As regras matematicas funcionam da mesma forma no JS

console.log(5+(2*4)); // resultado: 13

