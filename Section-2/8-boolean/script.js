/* BOOLEAN

Tipo boolean são usados para definir quais trechos do código serão executados ou repetidos, também são bastante usados
para verificar se uma condição é falsa ou verdadeira (true or false), sendo eles seus unicos valores possiveis.

    TRUE    -   VERDADEIRO
    FALSE   -   FALSO

Com os numeros booleanos é possivel guardar valores de uma comparação, exemplo:
*/

console.log(5 > 2); // TRUE
console.log(typeof(5 > 2)); // TRUE
console.log(5 < 2); // FALSE
console.log(typeof(5 < 2)); // FALSE