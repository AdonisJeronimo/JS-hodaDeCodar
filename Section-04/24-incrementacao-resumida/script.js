/*
Incrementando a varivael: forma facil

Para incrementar uma variavel de forma simpples, podemos utilziar duas opções

    x += 1; DESTA FORMAS PODEMOS ESCOLHER QUAL NUMERO QUEMOS SOMAR
    x ++; AQUI SERA FEITO UM ACRESCIMO DE UM EM UM
*/

for (let numero = 0; numero < 10; numero +=1){
    console.log(`O numero é: ${numero}`);
};

console.log("-----")

for ( let numero = 0; numero < 15; numero ++ ){
    console.log(`O numero é: $(numero)`)
}

/*

 Operadores de ATRIBUIÇÃO

    =   ATRIBUIÇÃO                      A = 10
    +=  ATRIBUIÇÃO DE SOMA              A += B  É 30    ,SENDO O MESMO QUE A = A + B
    -=  ATRIBUIÇÃO DE SUBTRAÇÃO         A -= B  É -10   ,SENDO O MESMO QUE A = A - B
    *=  ATRIBUIÇÃO DE MULTIPLICAÇÃO     A *= B  É 200   ,SENDO O MESMO QUE A = A * B
    /=  ATRIBUIÇÃO DE DIVISÃO           A /= B  É 0.5   ,SENDO O MESMO QUE A = A / B
    %=  ATRIBUIÇÃO DE RESTO             A %= B  É 10    ,SENDO O MESMO QUE A = A % B

    ++  INCREMENTO(+1)                  A++ = 11
    --  DECREMENTO(-1)                  A-- = 9

*/


