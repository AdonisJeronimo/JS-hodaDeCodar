/*
Metodo de arrays: SLICE

METODO SPLICE   -   Ira retornar um array a partir de outro array, ou seja, neste metodos podemos pegar algum elemento de algum array e colocar em outra variavel. COnseguimos pegar um elemento especifico do array.
O array retornado é determinado pelos parametrso que são os indices de inicio e fim do novo array
*/

let numeros = [0,1,2,3,4,5];

console.log(numeros)
console.log(numeros.slice(2,3));
console.log(numeros.slice(2,4));
console.log(numeros.slice(3));

