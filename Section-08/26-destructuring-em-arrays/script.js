/*
Destructuring em arrays

Podemos definir variaveis com os valores de um array utilizando o destructuring;


*/

let nomes = ["Matheus","João","Pedro"];
let [nomeA, nomeB, nomeC] = nomes;
console.log(nomeA);
console.log(nomeB);
console.log(nomeC);

